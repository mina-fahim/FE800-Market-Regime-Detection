"""Regression tests for Inflation_Model_Standard (OU / Vasicek process).

These tests verify that the stochastic inflation model outputs do not change
unexpectedly across code changes, package upgrades, or refactors.

Test Strategy
-------------
Each test:
1. Loads a pre-computed baseline Parquet file (generated by ``generate_baselines.py``).
2. Runs the same model operations with identical fixed-seed input data.
3. Compares every value against the baseline within the stated tolerance.

Tolerances
----------
* **Fitted parameters** (OLS on differences): ``abs=1e-10`` — deterministic if the
  same input data is used.
* **Monte Carlo predictions** (seeded): ``abs=1e-8`` — the RNG seed is pinned (99),
  giving fully reproducible paths on the same platform.
* **Analytical formulas**: ``abs=1e-12`` — closed-form, no stochastic component.

Baseline Regeneration
---------------------
If a test fails because of an *intentional* algorithmic change::

    python tests/regression_data/inflation/generate_baselines.py

Then commit both the code changes and the updated Parquet files together.
"""

from __future__ import annotations

import logging

import polars as pl
import pytest

from src.models.inflation.model_inflation_standard import Inflation_Model_Standard  # noqa: TC001

from tests.tests_regression.models.inflation.conftest import (
    PREDICT_SEED,
    load_baseline,
)


logger = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# Tolerances
# ---------------------------------------------------------------------------
_TOL_PARAMETERS: float = 1e-10
_TOL_MONTE_CARLO: float = 1e-8
_TOL_ANALYTICAL: float = 1e-12


# =============================================================================
# Helper
# =============================================================================


def _assert_dataframe_matches_baseline(
    current: pl.DataFrame,
    baseline: pl.DataFrame,
    *,
    tolerance: float,
    label: str = "",
) -> None:
    """Compare two Polars DataFrames element-by-element within tolerance.

    Parameters
    ----------
    current:
        DataFrame produced by re-running the model in this test session.
    baseline:
        DataFrame loaded from the Parquet baseline file.
    tolerance:
        Absolute tolerance for numeric comparisons.
    label:
        Optional label used in error messages.

    Raises
    ------
    AssertionError
        If shapes differ or any numeric value deviates beyond ``tolerance``.
    """
    assert current.shape == baseline.shape, (
        f"Shape mismatch {label}: current={current.shape}, baseline={baseline.shape}"
    )

    assert current.columns == baseline.columns, (
        f"Column mismatch {label}: current={current.columns}, baseline={baseline.columns}"
    )

    mismatches: list[str] = []
    for col in current.columns:
        if current[col].dtype in (pl.Float32, pl.Float64):
            for i in range(len(current)):
                c_val = float(current[col][i])
                b_val = float(baseline[col][i])
                diff = abs(c_val - b_val)
                if diff > tolerance:
                    mismatches.append(
                        f"  [{i}] {col}: baseline={b_val:.12e}  "
                        f"current={c_val:.12e}  diff={diff:.2e}",
                    )

    assert not mismatches, f"Regression failure {label} (tolerance={tolerance:.2e}):\n" + "\n".join(
        mismatches
    )


