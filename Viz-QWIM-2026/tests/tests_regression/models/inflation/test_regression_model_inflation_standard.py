@pytest.mark.regression()
class Class_Test_Regression_Standard_Analytical:
    """Regression: closed-form analytical mean and variance formulas."""

    def Test_Analytical_Mean_Variance_Match_Baseline(
        self,
        fitted_standard_annual: Inflation_Model_Standard,
    ) -> None:
        """Analytical mean and variance at multiple horizons must match baseline."""
        horizons = [0.5, 1.0, 2.0, 5.0, 10.0]
        rows = []
        for t_yr in horizons:
            mean_val = fitted_standard_annual.get_analytical_mean(t_yr)
            var_val = fitted_standard_annual.get_analytical_variance(t_yr)
            rows.append(
                {
                    "T_Years": t_yr,
                    "Analytical_Mean": mean_val,
                    "Analytical_Variance": var_val,
                },
            )
        df_current = pl.DataFrame(rows)
        df_baseline = load_baseline("standard_analytical_mean_variance")

        _assert_dataframe_matches_baseline(
            df_current,
            df_baseline,
            tolerance=_TOL_ANALYTICAL,
            label="standard_analytical_mean_variance",
        )

    def Test_Analytical_Variance_Monotonically_Increasing(
        self,
        fitted_standard_annual: Inflation_Model_Standard,
    ) -> None:
        """Variance must increase with time horizon (OU mean-reverting)."""
        horizons = [0.5, 1.0, 2.0, 5.0, 10.0, 20.0]
        variances = [fitted_standard_annual.get_analytical_variance(t) for t in horizons]
        for i in range(1, len(variances)):
            assert variances[i] >= variances[i - 1], (
                f"Variance decreased from t={horizons[i - 1]} to t={horizons[i]}: "
                f"{variances[i - 1]:.8e} > {variances[i]:.8e}"
            )

    def Test_Analytical_Variance_Near_Zero_Is_Near_Zero(
        self,
        fitted_standard_annual: Inflation_Model_Standard,
    ) -> None:
        """Variance at very small t must be near zero."""
        var_small = fitted_standard_annual.get_analytical_variance(1e-6)
        assert var_small == pytest.approx(0.0, abs=1e-6)

    def Test_Analytical_Mean_Near_Zero_Equals_Current_Rate(
        self,
        fitted_standard_annual: Inflation_Model_Standard,
    ) -> None:
        """Mean at very small t must be approximately the current rate."""
        mean_small = fitted_standard_annual.get_analytical_mean(1e-6)
        current_rate = fitted_standard_annual.parameters["current_rate"]
        assert mean_small == pytest.approx(current_rate, abs=1e-6)

    def Test_Analytical_Mean_At_Large_T_Equals_Theta(
        self,
        fitted_standard_annual: Inflation_Model_Standard,
    ) -> None:
        """Mean at very large t must converge to theta (long-run mean)."""
        mean_100 = fitted_standard_annual.get_analytical_mean(100.0)
        theta = fitted_standard_annual.parameters["theta"]
        assert mean_100 == pytest.approx(theta, abs=1e-6)

    def Test_Analytical_Variance_Converges_To_Stationary(
        self,
        fitted_standard_annual: Inflation_Model_Standard,
    ) -> None:
        r"""Variance at large t must converge to stationary value :math:`\sigma^2/(2\kappa)`.

        The OU process has stationary variance $\frac{\sigma^2}{2\kappa}$.
        """
        params = fitted_standard_annual.parameters
        kappa = params["kappa"]
        sigma = params["sigma"]
        stationary_var = sigma**2 / (2.0 * kappa)
        var_100 = fitted_standard_annual.get_analytical_variance(100.0)
        assert var_100 == pytest.approx(stationary_var, abs=1e-6)
