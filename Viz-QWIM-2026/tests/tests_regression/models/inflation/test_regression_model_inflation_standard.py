@pytest.mark.regression()
class Class_Test_Regression_Standard_Annual:
    """Regression: standard OU model fitted on canonical 10-year annual data."""

    def Test_Parameters_Match_Baseline(
        self,
        fitted_standard_annual: Inflation_Model_Standard,
    ) -> None:
        """Fitted parameters (kappa, theta, sigma) must match baseline."""
        params = fitted_standard_annual.parameters
        params_df = pl.DataFrame({key: [value] for key, value in params.items()})
        baseline_df = load_baseline("standard_fitted_annual_parameters")

        _assert_dataframe_matches_baseline(
            params_df,
            baseline_df,
            tolerance=_TOL_PARAMETERS,
            label="standard_fitted_annual_parameters",
        )

    def Test_Predict_12_Months_Matches_Baseline(
        self,
        fitted_standard_annual: Inflation_Model_Standard,
    ) -> None:
        """12-month seeded prediction must match stored baseline."""
        df_current = fitted_standard_annual.predict(
            n_periods=12,
            start_date="2025-01-01",
            freq="1mo",
            seed=PREDICT_SEED,
        )
        df_baseline = load_baseline("standard_fitted_annual_predict_12mo")

        _assert_dataframe_matches_baseline(
            df_current,
            df_baseline,
            tolerance=_TOL_MONTE_CARLO,
            label="standard_fitted_annual_predict_12mo",
        )

    def Test_Predict_Row_Count_Is_12(
        self,
        fitted_standard_annual: Inflation_Model_Standard,
    ) -> None:
        """Prediction must contain exactly 12 rows."""
        df_current = fitted_standard_annual.predict(
            n_periods=12,
            start_date="2025-01-01",
            freq="1mo",
            seed=PREDICT_SEED,
        )
        assert len(df_current) == 12

    def Test_Predict_Columns_Are_Correct(
        self,
        fitted_standard_annual: Inflation_Model_Standard,
    ) -> None:
        """Prediction must have the expected statistical summary columns."""
        df_current = fitted_standard_annual.predict(
            n_periods=12,
            start_date="2025-01-01",
            freq="1mo",
            seed=PREDICT_SEED,
        )
        expected_cols = {"Date", "Mean", "Median", "Std", "P5", "P25", "P75", "P95", "Min", "Max"}
        assert set(df_current.columns) == expected_cols

    def Test_Summary_Statistics_Match_Baseline(
        self,
        fitted_standard_annual: Inflation_Model_Standard,
    ) -> None:
        """Aggregate summary of prediction must match baseline."""
        df_predict = fitted_standard_annual.predict(
            n_periods=12,
            start_date="2025-01-01",
            freq="1mo",
            seed=PREDICT_SEED,
        )
        summary_df = pl.DataFrame(
            {
                "Mean_Rate": [df_predict["Mean"].mean()],
                "Median_Rate": [df_predict["Median"].mean()],
                "Mean_Std": [df_predict["Std"].mean()],
                "Mean_P5": [df_predict["P5"].mean()],
                "Mean_P95": [df_predict["P95"].mean()],
            },
        )
        baseline_df = load_baseline("standard_fitted_annual_summary")

        _assert_dataframe_matches_baseline(
            summary_df,
            baseline_df,
            tolerance=_TOL_MONTE_CARLO,
            label="standard_fitted_annual_summary",
        )

    def Test_Get_Annual_Rate_Returns_Theta(
        self,
        fitted_standard_annual: Inflation_Model_Standard,
    ) -> None:
        """get_annual_rate() must return theta (long-run mean)."""
        rate = fitted_standard_annual.get_annual_rate()
        params = fitted_standard_annual.parameters
        assert rate == pytest.approx(params["theta"], abs=_TOL_PARAMETERS)

    def Test_Seeded_Predict_Is_Reproducible(
        self,
        fitted_standard_annual: Inflation_Model_Standard,
    ) -> None:
        """Two consecutive seeded predictions must yield identical results."""
        df_a = fitted_standard_annual.predict(
            n_periods=12,
            start_date="2025-01-01",
            freq="1mo",
            seed=PREDICT_SEED,
        )
        df_b = fitted_standard_annual.predict(
            n_periods=12,
            start_date="2025-01-01",
            freq="1mo",
            seed=PREDICT_SEED,
        )
        _assert_dataframe_matches_baseline(
            df_a,
            df_b,
            tolerance=1e-15,
            label="reproducibility_check",
        )


