@pytest.mark.regression()
class Class_Test_Regression_Vasicek_Analytical:
    """Regression tests for Vasicek analytical mean and variance formulas."""

    def Test_Analytical_Mean_Matches_Baseline(
        self,
        fitted_vasicek: Interest_Rate_Model_Standard,
    ) -> None:
        """Analytical conditional mean must match stored baseline."""
        baseline = load_baseline("vasicek_analytical_mean")
        for idx_row in range(len(baseline)):
            t_val = float(baseline["t_years"][idx_row])
            expected = float(baseline["analytical_mean"][idx_row])
            actual = fitted_vasicek.get_analytical_mean(t_years=t_val)
            assert actual == pytest.approx(expected, abs=_TOL_NUMERICAL), (
                f"Analytical mean at t={t_val} differs: {actual} vs {expected}"
            )

    def Test_Analytical_Variance_Matches_Baseline(
        self,
        fitted_vasicek: Interest_Rate_Model_Standard,
    ) -> None:
        """Analytical conditional variance must match stored baseline."""
        baseline = load_baseline("vasicek_analytical_variance")
        for idx_row in range(len(baseline)):
            t_val = float(baseline["t_years"][idx_row])
            expected = float(baseline["analytical_variance"][idx_row])
            actual = fitted_vasicek.get_analytical_variance(t_years=t_val)
            assert actual == pytest.approx(expected, abs=_TOL_NUMERICAL), (
                f"Analytical variance at t={t_val} differs: {actual} vs {expected}"
            )

    def Test_Variance_Increases_With_Horizon(
        self,
        fitted_vasicek: Interest_Rate_Model_Standard,
    ) -> None:
        """Conditional variance must be non-decreasing in time."""
        variances = [
            fitted_vasicek.get_analytical_variance(t_years=t_val) for t_val in ANALYTICAL_HORIZONS
        ]
        for idx_pair in range(len(variances) - 1):
            assert variances[idx_pair] <= variances[idx_pair + 1] + _TOL_NUMERICAL

    def Test_Variance_Is_Non_Negative(
        self,
        fitted_vasicek: Interest_Rate_Model_Standard,
    ) -> None:
        """Conditional variance must be non-negative for all horizons."""
        for t_val in ANALYTICAL_HORIZONS:
            assert fitted_vasicek.get_analytical_variance(t_years=t_val) >= 0

    def Test_Mean_Converges_To_Theta(
        self,
        fitted_vasicek: Interest_Rate_Model_Standard,
    ) -> None:
        """At long horizon the conditional mean approaches theta."""
        long_horizon_mean = fitted_vasicek.get_analytical_mean(t_years=100.0)
        assert long_horizon_mean == pytest.approx(
            fitted_vasicek.m_theta,
            abs=1e-6,
        )

    def Test_Stationary_Variance_Limit(
        self,
        fitted_vasicek: Interest_Rate_Model_Standard,
    ) -> None:
        r"""At long horizon, variance approaches sigma^2 / (2*kappa)."""
        long_var = fitted_vasicek.get_analytical_variance(t_years=100.0)
        stationary_var = fitted_vasicek.m_sigma**2 / (2.0 * fitted_vasicek.m_kappa)
        assert long_var == pytest.approx(stationary_var, abs=1e-6)


