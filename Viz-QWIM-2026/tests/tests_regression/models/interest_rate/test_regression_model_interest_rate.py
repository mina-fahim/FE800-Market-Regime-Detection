# ---------------------------------------------------------------------------
# Tolerances
# ---------------------------------------------------------------------------
_TOL_DETERMINISTIC: float = 1e-10  # constant model: exact match
_TOL_NUMERICAL: float = 1e-8  # floating-point rounding
_TOL_STOCHASTIC: float = 1e-6  # seeded Monte Carlo (platform variance)


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _assert_dataframe_matches_baseline(
    actual: pl.DataFrame,
    baseline: pl.DataFrame,
    *,
    tolerance: float = _TOL_DETERMINISTIC,
    skip_date_col: bool = True,
) -> None:
    """Assert that *actual* matches *baseline* element-wise within *tolerance*.

    Parameters
    ----------
    actual : pl.DataFrame
        Freshly computed DataFrame.
    baseline : pl.DataFrame
        Baseline loaded from Parquet.
    tolerance : float
        Absolute tolerance for numeric columns.
    skip_date_col : bool
        If ``True``, skip comparison of the ``Date`` column (date ranges
        may differ in dtype but represent the same dates).
    """
    assert actual.shape == baseline.shape, (
        f"Shape mismatch: actual {actual.shape} vs baseline {baseline.shape}"
    )
    assert actual.columns == baseline.columns, (
        f"Column mismatch: actual {actual.columns} vs baseline {baseline.columns}"
    )

    for col_name in actual.columns:
        if skip_date_col and col_name == "Date":
            continue
        actual_vals = actual[col_name].to_numpy()
        baseline_vals = baseline[col_name].to_numpy()
        np.testing.assert_allclose(
            actual_vals,
            baseline_vals,
            atol=tolerance,
            rtol=0,
            err_msg=f"Column '{col_name}' differs from baseline",
        )


