"""Regression tests for Scenarios_Distribution and Scenarios_CMA.

These tests verify that scenario outputs do not change unexpectedly across
code changes, package upgrades, or refactors.

Test Strategy
-------------
Each test:
1. Loads a pre-computed baseline Parquet file (generated by
   ``generate_baselines.py``).
2. Runs the same scenario with the identical fixed-seed input data.
3. Compares results against the baseline within the stated tolerance.

Tolerances
----------
* **Scenario DataFrames** (seeded Normal/Student-t/CMA paths): ``abs=1e-8``
  â€” results must be bit-for-bit identical with fixed RNG seed.
* **Summary statistics** (mean, std, skew, â€¦): ``abs=1e-6``
  â€” floating-point accumulation may introduce tiny noise.
* **Lognormal** paths: ``abs=1e-6`` â€” exp() amplifies tiny float noise.
* **CMA daily conversions**: ``abs=1e-10`` â€” simple division with no
  accumulation.

Baseline regeneration
---------------------
If a test fails because of an *intentional* algorithmic change::

    python tests/regression_data/scenarios/generate_baselines.py

Then commit both the code changes and the updated Parquet files together.

Markers
-------
All tests carry ``@pytest.mark.regression``.
"""

