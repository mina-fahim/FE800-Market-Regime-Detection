def _assert_dataframe_matches_baseline(
    df_actual: pl.DataFrame,
    baseline_name: str,
    *,
    tolerance: float = _TOL_EXACT,
) -> None:
    """Assert that *df_actual* matches the stored Parquet baseline.

    Parameters
    ----------
    df_actual : pl.DataFrame
        The DataFrame produced by the current codebase.
    baseline_name : str
        Parquet basename (without extension) in the regression data dir.
    tolerance : float
        Absolute tolerance for floating-point comparisons.
    """
    df_baseline = load_baseline(baseline_name)

    assert df_actual.shape == df_baseline.shape, (
        f"Shape mismatch for '{baseline_name}': "
        f"actual={df_actual.shape}, baseline={df_baseline.shape}"
    )

    assert list(df_actual.columns) == list(df_baseline.columns), (
        f"Column mismatch for '{baseline_name}': "
        f"actual={df_actual.columns}, baseline={df_baseline.columns}"
    )

    # Compare numeric columns within tolerance
    for col_name in df_actual.columns:
        dtype = df_actual.schema[col_name]
        if dtype in (pl.Float64, pl.Float32):
            vals_actual = df_actual[col_name].to_numpy()
            vals_baseline = df_baseline[col_name].to_numpy()
            np.testing.assert_allclose(
                vals_actual,
                vals_baseline,
                atol=tolerance,
                rtol=0,
                err_msg=f"Column '{col_name}' regression mismatch in '{baseline_name}'",
            )
        elif dtype == pl.Date:
            assert df_actual[col_name].to_list() == df_baseline[col_name].to_list(), (
                f"Date column '{col_name}' mismatch in '{baseline_name}'"
            )
        elif dtype == pl.Utf8 or dtype == pl.String:
            assert df_actual[col_name].to_list() == df_baseline[col_name].to_list(), (
                f"String column '{col_name}' mismatch in '{baseline_name}'"
            )


