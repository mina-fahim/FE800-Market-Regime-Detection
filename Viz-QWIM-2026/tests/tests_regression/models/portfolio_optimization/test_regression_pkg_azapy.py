@pytest.mark.regression()
@_skip_if_no_azapy
class Class_Test_Regression_Azapy_Cross_Method_Consistency:
    """Verify logical consistency across different azapy optimization methods."""

    def Test_Regression_Inverse_Vol_Matches_Skfolio(
        self,
        canonical_returns_data: pl.DataFrame,
    ) -> None:
        """Azapy inverse-vol and skfolio inverse-vol should produce similar weights.

        Both methods compute 1/sigma_i weights — difference should be small.
        Minor deviations are expected due to different variance estimators.
        """
        azapy_portfolio = calc_azapy_inverse_volatility(
            returns_data=canonical_returns_data,
            portfolio_name="cross_azapy_inv_vol",
            optimization_date=FIXED_DATE,
        )
        azapy_weights = azapy_portfolio.get_portfolio_weights()

        skfolio_baseline = load_baseline("skfolio_basic_inverse_volatility")

        asset_cols = [col for col in azapy_weights.columns if col != "Date"]
        for asset in asset_cols:
            azapy_w = float(azapy_weights[asset][0])
            skfolio_w = float(skfolio_baseline[asset][0])
            diff = abs(azapy_w - skfolio_w)
            # Wider tolerance: different estimators may differ by ~1-5%
            assert diff < 0.10, (
                f"Azapy vs skfolio inverse-vol for {asset}: "
                f"azapy={azapy_w:.6f}, skfolio={skfolio_w:.6f}, diff={diff:.4f}"
            )

    def Test_Regression_Min_Risk_Methods_Differ(
        self,
        canonical_returns_data: pl.DataFrame,
    ) -> None:
        """Different MinRisk methods (MV, CVaR, MAD) should produce different weights.

        While all minimize risk, different risk measures lead to different allocations.
        """
        mv_portfolio = calc_azapy_mean_variance(
            returns_data=canonical_returns_data,
            rtype="MinRisk",
            portfolio_name="cross_mv_min_risk",
            optimization_date=FIXED_DATE,
        )
        cvar_portfolio = calc_azapy_cvar(
            returns_data=canonical_returns_data,
            alpha=0.975,
            rtype="MinRisk",
            portfolio_name="cross_cvar_min_risk",
            optimization_date=FIXED_DATE,
        )

        mv_weights = mv_portfolio.get_portfolio_weights()
        cvar_weights = cvar_portfolio.get_portfolio_weights()

        asset_cols = [col for col in mv_weights.columns if col != "Date"]
        total_diff = sum(
            abs(float(mv_weights[col][0]) - float(cvar_weights[col][0])) for col in asset_cols
        )
        # MV and CVaR usually produce different allocations
        assert total_diff > 1e-6, (
            f"MV and CVaR MinRisk should differ but total weight diff is {total_diff:.8f}"
        )

    def Test_Regression_All_Methods_Valid_Portfolios(
        self,
        canonical_returns_data: pl.DataFrame,
    ) -> None:
        """All azapy methods must produce valid portfolios (weights sum to 1, non-negative)."""
        methods_and_kwargs: list[tuple[str, Any, dict[str, Any]]] = [
            ("MV_MinRisk", calc_azapy_mean_variance, {"rtype": "MinRisk"}),
            ("CVaR_MinRisk", calc_azapy_cvar, {"alpha": 0.975, "rtype": "MinRisk"}),
            ("MAD_MinRisk", calc_azapy_mad, {"rtype": "MinRisk"}),
            ("InvVol", calc_azapy_inverse_volatility, {}),
            ("Kelly", calc_azapy_kelly, {"rtype": "ExpCone"}),
            ("EVaR_MinRisk", calc_azapy_evar, {"alpha": 0.975, "rtype": "MinRisk"}),
        ]

        for method_label, func, kwargs in methods_and_kwargs:
            portfolio = func(
                returns_data=canonical_returns_data,
                portfolio_name=f"cross_all_{method_label}",
                optimization_date=FIXED_DATE,
                **kwargs,
            )
            weights_df = portfolio.get_portfolio_weights()
            asset_cols = [col for col in weights_df.columns if col != "Date"]

            weight_sum = sum(float(weights_df[col][0]) for col in asset_cols)
            assert abs(weight_sum - 1.0) < _TOL_WEIGHT_SUM, (
                f"{method_label}: weights sum to {weight_sum:.8f}"
            )

            for asset in asset_cols:
                weight_val = float(weights_df[asset][0])
                assert weight_val >= -1e-8, (
                    f"{method_label}: {asset} weight is negative ({weight_val:.8f})"
                )
