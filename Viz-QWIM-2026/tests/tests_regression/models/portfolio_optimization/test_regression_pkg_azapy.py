# ---------------------------------------------------------------------------
# Tolerance constants
# ---------------------------------------------------------------------------
_TOL_DETERMINISTIC: float = 1e-6
_TOL_SOLVER: float = 1e-5
_TOL_WEIGHT_SUM: float = 1e-5


# ---------------------------------------------------------------------------
# Comparison helpers
# ---------------------------------------------------------------------------


def _assert_weights_match_baseline(
    portfolio: object,
    baseline_name: str,
    *,
    tolerance: float = _TOL_DETERMINISTIC,
) -> None:
    """Compare portfolio weights against the stored Parquet baseline.

    Parameters
    ----------
    portfolio:
        Result returned by an azapy optimization function (``portfolio_QWIM``).
    baseline_name:
        Key used to look up the Parquet file (without ``.parquet`` extension).
    tolerance:
        Absolute tolerance for per-asset weight comparison.

    Raises
    ------
    AssertionError
        If any weight deviates from the baseline beyond ``tolerance``.
    FileNotFoundError
        If the baseline Parquet file does not yet exist.
    """
    baseline_df: pl.DataFrame = load_baseline(baseline_name)
    current_weights_df: pl.DataFrame = portfolio.get_portfolio_weights()  # type: ignore[union-attr]

    baseline_asset_cols = [col for col in baseline_df.columns if col != "Date"]
    current_asset_cols = [col for col in current_weights_df.columns if col != "Date"]

    assert set(baseline_asset_cols) == set(current_asset_cols), (
        f"Asset columns differ!\n"
        f"  Baseline: {baseline_asset_cols}\n"
        f"  Current:  {current_asset_cols}"
    )

    mismatches: list[str] = []
    for asset in baseline_asset_cols:
        baseline_w = float(baseline_df[asset][0])
        current_w = float(current_weights_df[asset][0])
        diff = abs(current_w - baseline_w)
        if diff > tolerance:
            mismatches.append(
                f"  {asset}: baseline={baseline_w:.8f}  current={current_w:.8f}  "
                f"diff={diff:.2e}  tol={tolerance:.2e}",
            )

    assert not mismatches, (
        f"Regression failure for '{baseline_name}' "
        f"(tolerance={tolerance:.2e}):\n" + "\n".join(mismatches)
    )

    weight_sum = sum(float(current_weights_df[col][0]) for col in current_asset_cols)
    assert abs(weight_sum - 1.0) < _TOL_WEIGHT_SUM, (
        f"Weights do not sum to 1.0 (sum={weight_sum:.8f})"
    )


def _assert_portfolio_properties(
    portfolio: object,
    *,
    expected_num_assets: int = 5,
) -> None:
    """Validate common structural properties of a portfolio result.

    Parameters
    ----------
    portfolio:
        Portfolio object returned by an optimization function.
    expected_num_assets:
        Expected number of asset columns.
    """
    weights_df: pl.DataFrame = portfolio.get_portfolio_weights()  # type: ignore[union-attr]
    asset_cols = [col for col in weights_df.columns if col != "Date"]

    # Correct number of assets
    assert len(asset_cols) == expected_num_assets, (
        f"Expected {expected_num_assets} assets, got {len(asset_cols)}: {asset_cols}"
    )

    # Single row of weights
    assert len(weights_df) == 1, f"Expected 1 row of weights, got {len(weights_df)}"

    # Weights are finite
    for col in asset_cols:
        weight_val = float(weights_df[col][0])
        assert np.isfinite(weight_val), f"Weight for {col} is not finite: {weight_val}"

    # Weights sum to 1
    weight_sum = sum(float(weights_df[col][0]) for col in asset_cols)
    assert abs(weight_sum - 1.0) < _TOL_WEIGHT_SUM, f"Weights sum to {weight_sum:.8f}, expected 1.0"


