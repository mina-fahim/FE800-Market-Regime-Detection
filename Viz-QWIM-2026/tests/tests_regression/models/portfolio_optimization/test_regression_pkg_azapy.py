# pyright: reportPossiblyUnbound=false
"""Regression tests for azapy portfolio optimization methods.

These tests verify that optimization results from the azapy package wrappers
do not change unexpectedly across code changes, package upgrades, or refactors.

Test Strategy
-------------
Each test:
1. Loads a pre-computed baseline Parquet file (generated by generate_baselines.py).
2. Runs the same optimization with the identical fixed-seed input data.
3. Compares every asset weight against the baseline within the stated tolerance.

Tolerances
----------
* **Deterministic methods** (mean-variance MinRisk, inverse volatility, MAD, etc.):
  ``abs=1e-6``  — results must be identical within floating-point noise.
* **Solver-sensitive methods** (CVaR, EVaR, Kelly): ``abs=1e-5`` — solvers may
  exhibit minor numerical variation across platforms and versions.

Baseline regeneration
---------------------
If a test fails because of an *intentional* algorithmic change:

    python tests/regression_data/portfolio_optimization/generate_baselines.py

Then commit both the code changes and the updated Parquet files together.

Coverage
--------
Functions tested:
    - ``calc_azapy_mean_variance``  (MinRisk, Sharpe)
    - ``calc_azapy_cvar``           (MinRisk, Sharpe at alpha=0.975)
    - ``calc_azapy_mad``            (MinRisk, Sharpe)
    - ``calc_azapy_inverse_volatility``
    - ``calc_azapy_kelly``          (ExpCone)
    - ``calc_azapy_evar``           (MinRisk, Sharpe at alpha=0.975)
"""

