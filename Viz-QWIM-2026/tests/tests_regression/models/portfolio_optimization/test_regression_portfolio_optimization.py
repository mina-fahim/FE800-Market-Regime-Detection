def _assert_weights_match_baseline(
    portfolio: portfolio_QWIM,
    baseline_name: str,
    *,
    tolerance: float = 1e-6,
) -> None:
    """Compare portfolio weights against the stored Parquet baseline.

    Parameters
    ----------
    portfolio:
        Result returned by an optimization function.
    baseline_name:
        Key used to look up the Parquet file (without ``.parquet`` extension).
    tolerance:
        Absolute tolerance for per-asset weight comparison.

    Raises
    ------
    AssertionError
        If any weight deviates from the baseline beyond ``tolerance``.
    FileNotFoundError
        If the baseline Parquet file does not yet exist — run generate_baselines.py.
    """
    # Load baseline
    baseline_df: pl.DataFrame = load_baseline(baseline_name)

    # Extract current weights
    current_weights_df: pl.DataFrame = portfolio.get_portfolio_weights()

    # Verify same assets in same order
    baseline_asset_cols = [c for c in baseline_df.columns if c != "Date"]
    current_asset_cols = [c for c in current_weights_df.columns if c != "Date"]

    assert set(baseline_asset_cols) == set(current_asset_cols), (
        f"Asset columns differ!\n"
        f"  Baseline: {baseline_asset_cols}\n"
        f"  Current:  {current_asset_cols}"
    )

    # Compare each weight
    mismatches: list[str] = []
    for asset in baseline_asset_cols:
        baseline_w = float(baseline_df[asset][0])
        current_w = float(current_weights_df[asset][0])
        diff = abs(current_w - baseline_w)
        if diff > tolerance:
            mismatches.append(
                f"  {asset}: baseline={baseline_w:.8f}  current={current_w:.8f}  "
                f"diff={diff:.2e}  tol={tolerance:.2e}",
            )

    assert not mismatches, (
        f"Regression failure for '{baseline_name}' "
        f"(tolerance={tolerance:.2e}):\n" + "\n".join(mismatches)
    )

    # Verify weights still sum to 1
    weight_sum = sum(float(current_weights_df[a][0]) for a in current_asset_cols)
    assert abs(weight_sum - 1.0) < 1e-5, f"Weights do not sum to 1.0 (sum={weight_sum:.8f})"


