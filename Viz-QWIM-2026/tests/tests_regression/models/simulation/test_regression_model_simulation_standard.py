"""Regression tests for the Simulation_Standard Monte Carlo simulation.

These tests verify that simulation outputs do not change unexpectedly across
code changes, package upgrades, or refactors.

Test Strategy
-------------
Each test:
1. Loads a pre-computed baseline Parquet file (generated by generate_baselines.py).
2. Runs the same simulation with the identical fixed-seed input data.
3. Compares results against the baseline within the stated tolerance.

Tolerances
----------
* **Full results** (seeded Normal/Student-t paths): ``abs=1e-8``
  — results must be bit-for-bit identical with fixed RNG seed.
* **Summary statistics** (mean, median, percentiles): ``abs=1e-6``
  — floating-point accumulation may introduce tiny noise.
* **Terminal values**: ``abs=1e-8``
  — single-row extraction, should match exactly.
* **Lognormal** paths: ``abs=1e-6`` — exp() amplifies tiny float noise.

Baseline regeneration
---------------------
If a test fails because of an *intentional* algorithmic change::

    python tests/regression_data/simulation/generate_baselines.py

Then commit both the code changes and the updated Parquet files together.

Coverage
--------
Configurations tested:
    - Normal distribution, equal weights
    - Student-t distribution, equal weights
    - Lognormal distribution, equal weights
    - Normal distribution, unequal weights
    - Normal distribution, high initial value (1M)
"""

