# ---------------------------------------------------------------------------


def _assert_dataframe_matches_baseline(
    current: pl.DataFrame,
    baseline_name: str,
    *,
    tolerance: float = _TOL_EXACT,
    compare_cols: list[str] | None = None,
) -> None:
    """Compare a DataFrame against its Parquet baseline.

    Parameters
    ----------
    current:
        DataFrame produced by the current simulation run.
    baseline_name:
        Parquet file key (without ``.parquet`` extension).
    tolerance:
        Absolute tolerance for numeric comparison.
    compare_cols:
        Specific columns to compare.  ``None`` → compare all numeric columns.

    Raises
    ------
    AssertionError
        If any value deviates beyond ``tolerance``.
    """
    baseline = load_baseline(baseline_name)

    if compare_cols is None:
        compare_cols = [col for col in baseline.columns if col != "Date"]

    # Shape check
    assert current.shape[0] == baseline.shape[0], (
        f"Row count mismatch for '{baseline_name}': "
        f"current={current.shape[0]}, baseline={baseline.shape[0]}"
    )

    missing_cols = set(compare_cols) - set(current.columns)
    assert not missing_cols, (
        f"Missing columns in current results for '{baseline_name}': {missing_cols}"
    )

    mismatches: list[str] = []
    for col in compare_cols:
        if col not in baseline.columns:
            continue
        baseline_vals = baseline[col].to_numpy()
        current_vals = current[col].to_numpy()

        diffs = np.abs(current_vals - baseline_vals)
        max_diff = float(np.max(diffs))

        if max_diff > tolerance:
            idx_worst = int(np.argmax(diffs))
            mismatches.append(
                f"  {col}[{idx_worst}]: baseline={baseline_vals[idx_worst]:.10f}  "
                f"current={current_vals[idx_worst]:.10f}  "
                f"max_diff={max_diff:.2e}  tol={tolerance:.2e}",
            )

    assert not mismatches, (
        f"Regression failure for '{baseline_name}' "
        f"(tolerance={tolerance:.2e}):\n" + "\n".join(mismatches)
    )


