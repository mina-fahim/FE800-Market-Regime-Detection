def _validate_positive_float(value: float, name: str) -> None:
    """Raise if *value* is not a positive finite float.

    Parameters
    ----------
    value : float
        Value to check.
    name : str
        Parameter name for the error message.

    Raises
    ------
    Exception_Validation_Input
        If *value* is not a positive finite number.
    """
    if not isinstance(value, float | int) or not np.isfinite(value) or value <= 0:
        raise Exception_Validation_Input(
            f"{name} must be a positive finite number, got {value!r}",
            field_name=name,
            expected_type=float,
            actual_value=value,
        )


def _freq_to_dt_years(freq: str) -> float:
    """Convert a Polars frequency string to a fraction of a year.

    Parameters
    ----------
    freq : str
        Polars duration string (``"1d"``, ``"1mo"``, ``"1y"``).

    Returns
    -------
    float
        Equivalent fraction of a year.

    Raises
    ------
    Exception_Validation_Input
        If *freq* is not one of the supported values.
    """
    mapping: dict[str, float] = {
        "1d": 1.0 / 365.25,
        "1mo": 1.0 / 12.0,
        "1y": 1.0,
    }
    if freq not in mapping:
        raise Exception_Validation_Input(
            f"freq must be one of {sorted(mapping.keys())}, got '{freq}'",
            field_name="freq",
            expected_type=str,
            actual_value=freq,
        )
    return mapping[freq]


def _simulate_ou_exact(
    kappa: float,
    theta: float,
    sigma: float,
    r0: float,
    n_steps: int,
    n_paths: int,
    dt: float,
    rng: np.random.Generator,
) -> np.ndarray:
    r"""Exact simulation of the Vasicek (Ornstein-Uhlenbeck) process.

    Uses the analytical transition distribution:

    .. math::

        r_{t+\Delta t} \mid r_t
        \sim \mathcal{N}\!\left(
            \theta + (r_t - \theta)e^{-\kappa\Delta t},\;
            \frac{\sigma^2}{2\kappa}(1 - e^{-2\kappa\Delta t})
        \right)

    Parameters
    ----------
    kappa : float
        Mean-reversion speed.
    theta : float
        Long-run mean short rate.
    sigma : float
        Short-rate volatility.
    r0 : float
        Starting short rate.
    n_steps : int
        Number of time steps.
    n_paths : int
        Number of Monte Carlo paths.
    dt : float
        Time increment in years.
    rng : np.random.Generator
        NumPy random generator (for reproducibility).

    Returns
    -------
    np.ndarray
        Shape ``(n_steps, n_paths)`` array of simulated short rates.
    """
    e_kdt = np.exp(-kappa * dt)
    cond_var = (sigma**2 / (2.0 * kappa)) * (1.0 - np.exp(-2.0 * kappa * dt))
    cond_std = np.sqrt(max(cond_var, 0.0))

    paths = np.empty((n_steps, n_paths), dtype=np.float64)
    current = np.full(n_paths, r0, dtype=np.float64)

    for t in range(n_steps):
        cond_mean = theta + (current - theta) * e_kdt
        noise = rng.standard_normal(n_paths)
        current = cond_mean + cond_std * noise
        paths[t] = current

    return paths
