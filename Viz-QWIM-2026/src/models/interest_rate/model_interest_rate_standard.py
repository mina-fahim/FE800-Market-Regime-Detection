r"""Standard mean-reverting short-rate model (Vasicek / Ornstein-Uhlenbeck).

========================================================

Provides :class:`Interest_Rate_Model_Standard`, a stochastic model based
on the **Vasicek** (Ornstein-Uhlenbeck) short-rate process — the canonical
equilibrium term-structure model in the fixed-income literature.

The continuous-time specification is:

.. math::

    dr_t = \kappa (\theta - r_t)\, dt + \sigma\, dW_t

where :math:`\kappa > 0` is the mean-reversion speed, :math:`\theta`
is the long-run mean short rate, :math:`\sigma > 0` is the volatility,
and :math:`W_t` is a standard Brownian motion.

This is a Gaussian model; rates can in principle become negative, which
is empirically relevant for central-bank policy rates but may require
adjustment for nominal discount-rate applications.

Fitting
-------
Parameters are estimated from historical annual-rate data by an
**OLS regression on first differences** of the discretised OU process:

.. math::

    \Delta r_t = \alpha + \beta\, r_t + \varepsilon_t

so that :math:`\kappa = -\beta / \Delta t`, :math:`\theta = \alpha /
(-\beta)`, and :math:`\sigma = \mathrm{std}(\varepsilon) / \sqrt{\Delta t}`.

If the OLS estimate yields :math:`\hat\beta \ge 0` (non-mean-reverting
data), the model falls back to the constructor prior values.

Prediction
----------
Forward paths are generated by **exact simulation** of the OU process
(no Euler-Maruyama approximation error):

.. math::

    r_{t+\Delta t} = r_t e^{-\kappa\Delta t}
      + \theta(1 - e^{-\kappa\Delta t})
      + \sqrt{\frac{\sigma^2}{2\kappa}(1 - e^{-2\kappa\Delta t})}\,\varepsilon

The :class:`polars.DataFrame` returned by :meth:`predict` contains
per-period summary statistics across all Monte Carlo paths.

References
----------
* Vasicek, O. (1977). *An equilibrium characterization of the term
  structure.* Journal of Financial Economics, 5(2), 177-188.
* Brigo, D. & Mercurio, F. (2006). *Interest Rate Models — Theory and
  Practice.* Springer Finance.
* Hull, J. (2022). *Options, Futures, and Other Derivatives* (11th ed.).
  Pearson.

Author
------
QWIM Team

Version
-------
1.0.0 (2026-07-01)
"""

