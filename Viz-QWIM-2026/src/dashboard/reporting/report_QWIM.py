def compile_typst_report(
    typ_template_path: Path,
    output_pdf_path: Path,
) -> tuple[bool, str]:
    """Compile a Typst template to PDF.

    The template is expected to read its data from JSON files and SVG images
    via relative paths (``json("client_info.json")``, ``image(...)``).  All
    required files are copied into an ephemeral working directory so that
    Typst can resolve them.

    Parameters
    ----------
    typ_template_path : Path
        Absolute path to the ``.typ`` template.
    output_pdf_path : Path
        Desired path for the compiled PDF.

    Returns
    -------
    tuple[bool, str]
        ``(success, message)``.
    """
    if not typ_template_path.exists():
        return False, f"Typst template not found: {typ_template_path}"

    tmp_dir: Path | None = None
    try:
        output_pdf_path.parent.mkdir(parents=True, exist_ok=True)
        tmp_dir = Path(tempfile.mkdtemp(prefix="qwim_typst_"))

        template_dir = typ_template_path.parent

        # Copy the template itself
        shutil.copy2(typ_template_path, tmp_dir / typ_template_path.name)

        # Copy JSON files next to template
        for json_src in [
            template_dir / "client_info.json",
            template_dir / "report_metadata.json",
        ]:
            if json_src.exists():
                shutil.copy2(json_src, tmp_dir / json_src.name)

        # Copy JSON subdirectories
        for subdir_name in ("inputs_json", "outputs_json"):
            src_dir = template_dir / subdir_name
            if src_dir.is_dir():
                dst_dir = tmp_dir / subdir_name
                dst_dir.mkdir(exist_ok=True)
                for f in src_dir.iterdir():
                    if f.is_file():
                        shutil.copy2(f, dst_dir / f.name)

        # Copy SVG images
        img_src = template_dir / "outputs_images"
        if img_src.is_dir():
            img_dst = tmp_dir / "outputs_images"
            img_dst.mkdir(exist_ok=True)
            for f in img_src.iterdir():
                if f.is_file():
                    shutil.copy2(f, img_dst / f.name)

        # Copy any other assets (bib, fonts, etc.)
        for candidate in template_dir.iterdir():
            if candidate.is_file() and candidate.suffix in {
                ".bib",
                ".png",
                ".jpg",
                ".jpeg",
            }:
                dest = tmp_dir / candidate.name
                if not dest.exists():
                    shutil.copy2(candidate, dest)

        # Ensure bibliography stub exists
        bib_path = tmp_dir / "sample_refs.bib"
        if not bib_path.exists():
            bib_path.write_text("% Empty bibliography\n", encoding="utf-8")

        tmp_typ = tmp_dir / typ_template_path.name

        # Compile via typst Python package or CLI subprocess
        compiled = False
        compile_error = ""
        try:
            import typst as _typst

            _typst.compile(str(tmp_typ), output=str(output_pdf_path))
            compiled = True
        except ImportError:
            pass
        except Exception as typst_pkg_error:
            compile_error = str(typst_pkg_error)

        if not compiled:
            try:
                result = subprocess.run(
                    ["typst", "compile", str(tmp_typ), str(output_pdf_path)],
                    capture_output=True,
                    text=True,
                    timeout=120,
                    check=False,
                )
                if result.returncode == 0:
                    compiled = True
                else:
                    compile_error = result.stderr or result.stdout
            except FileNotFoundError:
                compile_error = (
                    "typst CLI not found. Install via `pip install typst` "
                    "or download from https://typst.app"
                )
            except subprocess.TimeoutExpired:
                compile_error = "Typst compilation timed out after 120 seconds"

        if not compiled:
            return False, f"Typst compilation failed: {compile_error}"

        if not output_pdf_path.exists():
            return False, "Compilation appeared to succeed but PDF not found"

        file_size = output_pdf_path.stat().st_size
        if file_size == 0:
            return False, "Compiled PDF is empty (0 bytes)"

        return True, f"Typst PDF compiled successfully ({file_size:,} bytes)"

    except Exception as exc:
        return False, f"Unexpected error during Typst compilation: {exc}"
    finally:
        if tmp_dir and tmp_dir.exists():
            shutil.rmtree(tmp_dir, ignore_errors=True)


