def test_report_generation() -> tuple[bool, str]:
    """Smoke-test PDF report generation with sample data.

    Returns
    -------
    tuple[bool, str]
        ``(success_flag, message)``.
    """
    try:
        logger.info("Testing report generation with sample data...")
        timestamp = datetime.datetime.now(datetime.UTC).strftime("%Y%m%d_%H%M%S")
        success, message, file_path = generate_report_PDF(
            report_title="Test QWIM Report",
            output_filename=f"test_report_{timestamp}.pdf",
            output_directory="outputs/test",
            reactives_shiny=None,
            user_id="test_user",
        )
        if success:
            return True, f"Test passed: {message}  {file_path}"
        return False, f"Test failed: {message}"
    except Exception as test_error:
        return False, f"Test error: {test_error}"


