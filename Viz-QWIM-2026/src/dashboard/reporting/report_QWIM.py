"""Shiny-Compatible PDF Report Generation Module for QWIM Analytics.

This module provides PDF report generation capabilities specifically designed
to be called from Shiny dashboard buttons following QWIM project coding standards.

Report generation uses the Typst typesetting system to compile professional
PDF documents from the ``report_QWIM.typ`` template.  Data is passed to
Typst via JSON files that the template reads with ``json()`` calls, and
charts are included as SVG images generated by ``plotnine``.

Pipeline
--------
1.  Export dashboard data → JSON files (``report_data_export``).
    Client information (``client_info.json``) is sourced directly from
    ``reactives_shiny["Data_Clients"]`` via
    ``utils_reporting.build_client_info_json_from_reactives()``.
    Subtab inputs/outputs (``inputs_json/*.json`` and
    ``outputs_json/*.json``) are sourced from
    ``reactives_shiny["Data_Results"]`` via
    ``utils_reporting.build_results_data_json_from_reactives()``.
    When a ``Data_Results`` key is not yet populated (``None``), each
    export function falls back to the legacy ``User_Inputs_Shiny`` /
    ``Inner_Variables_Shiny`` keys so that report generation always
    produces valid JSON.
2.  Export dashboard charts → SVG images (``report_plot_export``)
3.  Copy ``.typ`` template + JSON + SVG into a temp directory
4.  Compile ``report_QWIM.typ`` → PDF via ``typst`` Python bindings or CLI

Dependencies
-----------
* typst: Typst compilation system (CLI or Python bindings)
* polars: For high-performance DataFrames
* numpy: For numerical utilities
* plotnine: For static SVG chart generation

Shiny Integration
----------------
This module is designed to be called from Shiny server functions in the
"Reporting" subtab with proper error handling and status reporting.

Example Usage from Shiny
------------------------
    from src.dashboard.reporting.report_QWIM import generate_report_PDF

    Success_Flag, Output_Message, File_Path = generate_report_PDF(
        report_title="QWIM Portfolio Analysis",
        output_filename="qwim_report_20260301.pdf",
        reactives_shiny=Reactives_Shiny_Dict,
        user_id="user_001",
    )
"""

