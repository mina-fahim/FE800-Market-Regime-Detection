def generate_report_PDF(
    report_title: str = "QWIM Portfolio and Investor Analysis Report",
    output_filename: str = "qwim_report.pdf",
    output_directory: str = "outputs/reports",
    reactives_shiny: dict | None = None,
    user_id: str = "default_user",
    include_charts: bool = True,
    chart_resolution: str = "high",
) -> tuple[bool, str, str]:
    """Generate a QWIM PDF report using the Typst typesetting pipeline.

    Orchestrates:
    1. JSON data export (client info, subtab inputs/outputs)
    2. SVG chart generation via plotnine (when *include_charts* is ``True``)
    3. Typst template compilation to PDF

    Parameters
    ----------
    report_title : str
        Human-readable title (decorative — title is set in the template).
    output_filename : str
        Desired PDF filename including ``.pdf`` extension.
    output_directory : str
        Target directory for the output PDF.
    reactives_shiny : dict | None
        Shared reactive state dictionary from the Shiny application.
    user_id : str
        User identifier for logging.
    include_charts : bool
        Whether to generate SVG charts via plotnine.
    chart_resolution : str
        Reserved for future use.

    Returns
    -------
    tuple[bool, str, str]
        ``(success_flag, status_message, pdf_file_path)``.
    """
    logger.info("=" * 70)
    logger.info("PDF REPORT GENERATION  user: %s", user_id)
    logger.info("  title    : %s", report_title)
    logger.info("  filename : %s", output_filename)
    logger.info("  directory: %s", output_directory)
    logger.info("=" * 70)

    # Input validation
    if not output_filename or not output_filename.strip():
        msg = "output_filename cannot be empty"
        logger.error(msg)
        return False, msg, ""

    if not output_filename.lower().endswith(".pdf"):
        output_filename = output_filename + ".pdf"

    # Locate Typst template
    typ_template = _REPORTING_DIR / "report_QWIM.typ"
    if not typ_template.exists():
        msg = f"Typst template not found at: {typ_template}"
        logger.error(msg)
        return False, msg, ""

    # Prepare output path
    output_dir = Path(output_directory)
    try:
        output_dir.mkdir(parents=True, exist_ok=True)
    except Exception as dir_error:
        msg = f"Cannot create output directory '{output_dir}': {dir_error}"
        logger.error(msg)
        return False, msg, ""

    output_pdf = output_dir / output_filename

    # --- Step 1: Export data to JSON ---
    try:
        export_all_report_data(reactives_shiny)
        logger.info("JSON data export completed")
    except Exception as data_error:
        msg = f"Failed to export report data: {data_error}"
        logger.error(msg)
        return False, msg, ""

    # --- Step 2: Generate SVG charts ---
    if include_charts:
        try:
            export_all_report_plots(reactives_shiny)
            logger.info("SVG chart export completed")
        except Exception as plot_error:
            logger.warning("Chart export failed (continuing without charts): %s", plot_error)

    # --- Step 3: Compile Typst ---
    success, compile_msg = compile_typst_report(typ_template, output_pdf)
    if success:
        abs_path = str(output_pdf.resolve())
        logger.info("Report saved to %s", abs_path)
        return True, compile_msg, abs_path

    logger.error("Compilation failed: %s", compile_msg)
    return False, compile_msg, ""


