def safe_is_finite_value(value: Any) -> bool:
    """Safely check if a value is finite.

    Parameters
    ----------
    value : Any
        Value to check for finiteness.

    Returns
    -------
    bool
        ``True`` if *value* is finite, ``False`` otherwise.
    """
    if value is None:
        return False
    if not isinstance(value, (int, float, np.number)):
        return False
    try:
        return bool(np.isfinite(value))
    except Exception:
        return False


def safe_polars_column_access(
    polars_DF: pl.DataFrame | None,
    column_name: str,
    operation: str = "sum",
    default_value: Any = 0,
) -> Any:
    """Safely access polars DataFrame columns with error handling.

    Parameters
    ----------
    polars_DF : pl.DataFrame | None
        Source polars DataFrame.
    column_name : str
        Name of column to access.
    operation : str
        Operation to perform (``"sum"``, ``"mean"``, ``"n_unique"``,
        ``"first"``, ``"count"``).
    default_value : Any
        Default value if operation fails.

    Returns
    -------
    Any
        Result of operation or *default_value*.
    """
    if polars_DF is None:
        logger.warning("DataFrame is None for column %s", column_name)
        return default_value
    if polars_DF.height == 0 or polars_DF.width == 0:
        logger.warning("DataFrame is empty for column %s", column_name)
        return default_value
    if column_name not in polars_DF.columns:
        logger.warning("Column '%s' not found. Available: %s", column_name, polars_DF.columns)
        return default_value

    try:
        numeric_types = (
            pl.Int64,
            pl.Int32,
            pl.Float64,
            pl.Float32,
            pl.Int8,
            pl.Int16,
            pl.UInt32,
            pl.UInt64,
        )
        col_dtype = polars_DF.schema[column_name]
        op_map: dict[str, str] = {
            "sum": "sum",
            "mean": "mean",
            "n_unique": "n_unique",
            "first": "first",
            "count": "count",
        }

        if operation in ("sum", "mean") and col_dtype not in numeric_types:
            logger.warning("Column '%s' is not numeric for %s", column_name, operation)
            return default_value

        if operation not in op_map:
            logger.warning("Unknown operation '%s' for column %s", operation, column_name)
            return default_value

        expr = getattr(pl.col(column_name), op_map[operation])()
        result = polars_DF.select(expr).item()
        return result if result is not None else default_value
    except Exception as column_error:
        logger.warning("Error accessing column '%s': %s", column_name, column_error)
        return default_value


def validate_polars_DF(
    polars_DF: pl.DataFrame | None,
    dataframe_name: str = "DataFrame",
) -> bool:
    """Validate polars DataFrame structure and content.

    Parameters
    ----------
    polars_DF : pl.DataFrame | None
        DataFrame to validate.
    dataframe_name : str
        Name of DataFrame for logging.

    Returns
    -------
    bool
        ``True`` if DataFrame is valid and usable.
    """
    if polars_DF is None:
        logger.warning("%s is None", dataframe_name)
        return False
    if not isinstance(polars_DF, pl.DataFrame):
        logger.warning("%s is not a polars DataFrame: %s", dataframe_name, type(polars_DF))
        return False
    if polars_DF.height == 0 or polars_DF.width == 0:
        logger.warning("%s is empty", dataframe_name)
        return False
    logger.info("%s is valid: %d rows, %d cols", dataframe_name, polars_DF.height, polars_DF.width)
    return True


